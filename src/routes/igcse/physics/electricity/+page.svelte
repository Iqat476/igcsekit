  <h1>Electricity</h1>
  <h2>Electrical Quantities</h2>
  <h3>Electric Charge</h3>
  <p>There are 2 types of charges:</p>
  <ul>
    <li>positive (+)</li>
    <li>negative (-)</li>
  </ul>
  <p>Same charges repel and opposite charges attract.</p>
  <p>Charging a body involves the addition or removal of electrons.</p>
  <h4>Electrical Conduction</h4>
  <p>Materials that can conduct electricity well are called conductors. They allow electrons to flow through them. All metals are good conductors of electricity but graphite is also a good conductor.</p>
  <p>Insulators are poor conductors. Some common examples of insulators include plastic, glass, and wood.</p>
  <h4>Electric Field</h4>
  <p>An electric field is a region in which an electric charge <strong>experiences a force</strong>. It is not a magnetic field.</p>
  <h3>Current</h3>
  <p>Current is the rate of flow of charge.</p>
