  <h1>Sensitivity</h1>
  <h2>Nervous Control</h2>
  <p>A nerve impulse is an <strong>electrical</strong> signal that passes along nerve cells called neurons.</p>
  <h3>The Human Nervous System</h3>
  <p>The nervous system can be split into 2 parts:</p>
  <ul>
    <li>
      <strong>Central</strong>: brain and spinal cord
    </li>
    <li>
      <strong>Peripheral</strong>: all the other nerves that connect to the central nervous system
    </li>
  </ul>
  <h3>Reflex Arc</h3>
  <p>There are 3 different types of neurons involved in the relfex arc.</p>
  <ul>
    <li>
      <strong>Sensory</strong>: long and have a cell body branching off the middle of the axon
    </li>
    <li>
      <strong>Relay</strong>: short and have a small cell body at one end with many dendrites branching off it
    </li>
    <li>
      <strong>Motor</strong>: long and have a large cell body at one end with long dendrites branching off it
    </li>
  </ul>
  <p>Relay neurons are located in the central nervous system and do not have myelin sheaths on their axons. Motor neurons are also located in the central nervous system.</p>
  <p>Reflex arcs are a way of responding to stimuli using effectors such as muscles and glands.</p>
  <h3>Voluntary and Involuntary Actions</h3>
  <p>Voluntary actions require conscious thought whereas involuntary actions are automatic.</p>
  <h2>Sense Organs</h2>
  <h3>Eye</h3>
  <p>The eye is made up of many parts.</p>
  <ul>
    <li>
      <strong>Cornea</strong>: refracts light
    </li>
    <li>
      <strong>Iris</strong>: controls how much light enters pupil
    </li>
    <li>
      <strong>Lens</strong>: focuses light onto retina
    </li>
    <li>
      <strong>Retina</strong>: contains light receptors that are sensitive to light from different colours
    </li>
    <li>
      <strong>Optic Nerve</strong>: carries impulses to the brain
    </li>
  </ul>
  <p>The <strong>pupil reflex</strong> is used to control the amount of light entering the eye. The action of the radial and circular muscles is described as <strong>antagonistic</strong> because they work opposite to each other.</p>
  <ul>
    <li>
      In bright light:
      <ul>
        <li>circular muscles contract</li>
        <li>radial muscles relax</li>
        <li>pupil size decreases</li>
      </ul>
    </li>
    <li>
      In dim light:
      <ul>
        <li>circular muscles relax</li>
        <li>radial muscles contract</li>
        <li>pupil size increases</li>
      </ul>
    </li>
  </ul>
