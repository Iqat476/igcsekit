  <h1>Periodic Table</h1>
  <h2>The Periodic Table</h2>
  <p>The periodic table is used as a method of classifying and predicting the properties of elements.</p>
  <h2>Periodic Trends</h2>
  <p>Elements become more non-metallic across a period. Elements near the bottom left corner of the table are the most metallic while elements near the top right corner are the least metallic.</p>
  <p>An element's group number is equal to the number of electrons in its outer shell (valence electrons). For example, oxygen is in group 6 and has 6 valence electrons.</p>
  <h2>Group Properties</h2>
  <h3>Group 1</h3>
  <p>This is a collection of soft metals that react with water to produce an alkali and hydrogen.</p>
  <p>Going down a group:</p>
  <ul>
    <li>melting point decreases</li>
    <li>reactivity increases</li>
  </ul>
  <h3>Group 7</h3>
  <p>This is a collection of nonmetals and are also known as halogens.</p>
  <p>Going down a group:</p>
  <ul>
    <li>boiling point decreases</li>
    <li>reactivity decreases</li>
  </ul>
