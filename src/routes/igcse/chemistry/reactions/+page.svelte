  <h1>Reactions</h1>
  <h2>Rate of Reaction</h2>
  <p>The rate of reaction is the speed of a chemical reaction. It is how quickly reactant particles are converted into product particles.</p>
  <h3>Measuring Rate of Reaction</h3>
  <p>Some reactions produce a gas. There are a few ways to measure the rate of reaction in this situation. It is possible:</p>
  <ul>
    <li>collect the gas in a syringe and record its volume over fixed time intervals</li>
    <li>calculate the mass lost as gas over fixed time intervals</li>
  </ul>
  <p>Usually, it is easier to record the volume of gas instead of the mass lost.</p>
  <h3>Interpreting Data</h3>
  <p>Graphs are used to visualise the rate of reaction. The <strong>slope</strong> or <em>steepness</em> of the graphed curve represents the rate of reaction.</p>
  <p>The graph of a typical reaction with <strong>amount of reactants</strong> plotted against <strong>time</strong> would look like an exponentially decreasing curve. This is because the rate of reaction decreases as the amount of reactants decreases. The opposite of this graph would be <strong>amount of products</strong> against <strong>time</strong>.</p>
  <h3>Factors Affecting Rate of Reaction</h3>
  <p>All of these factors increase the rate of reaction.</p>
  <h4>Concentration</h4>
  <ul>
    <li>More reactant particles exist per unit volume</li>
    <li>More collisions occur per second</li>
  </ul>
  <h4>Temperature</h4>
  <ul>
    <li>Particles have more kinetic energy</li>
    <li>Particles collide with more force</li>
    <li>More particles have enough activation energy to react</li>
    <li>More <strong>successful</strong> collisions occur per second</li>
  </ul>
  <h4>Surface Area</h4>
  <ul>
    <li>More reactant particles are exposed to each other</li>
    <li>More collisions occur per second</li>
  </ul>
  <Callout emoji="ðŸ¤”">
