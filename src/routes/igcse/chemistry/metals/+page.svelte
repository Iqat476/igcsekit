  <h3>Electrolysis</h3>
  <p>Metals more reactive than carbon are extracted using electrolysis. For example, aluminium is extracted from bauxite by electrolysis. Aluminium has a <strong>high melting point</strong> that needs to be lowered by mixing it with <strong>cryolite</strong>.</p>
  <h3>Recycling</h3>
  <p>It is important to recycle metals because metal ores are a finite resource.</p>
  <h2>Uses of Metals</h2>
  <h3>Aluminium</h3>
  <p>Aluminium is used in:</p>
  <ul>
    <li>aircraft parts because of its strength and low density</li>
    <li>food containers because of its resistance to corrosion</li>
  </ul>
  <p>Aluminium has an apparent unreactivity and resistance to corrision because an oxide layer that adheres to the surface of aluminium. This layer prevents further oxidation.</p>
