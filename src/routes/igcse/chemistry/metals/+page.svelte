  <h1>Metals</h1>
  <h2>Properties</h2>
  <p>Metals are:</p>
  <ul>
    <li>solids with high melting and boiling points</li>
    <li>malleable</li>
    <li>good conductors of electricity and heat</li>
  </ul>
  <h3>Alloys</h3>
  <p>Alloys are mixtures of a metal with other elements.</p>
  <p>Alloys are used instead of pure metals because they may be:</p>
  <ul>
    <li>more resistant to corrossion</li>
    <li>stronger</li>
    <li>harder</li>
    <li>less dense</li>
  </ul>
  <p>Alloys contain atoms of <strong>different sizes</strong> which distorts the regular arrangement of atoms in metals. This makes it harder for layers to <strong>slide</strong> over each other.</p>
  <h2>Reactivity Series</h2>
  <p>The reactivity series is a list of metallic elements ranked their reactivity. Elements that are more reactive can <strong>displace</strong> less reactive elements from their compounds, forming a new compound.</p>
  <p>The reactivity series (starting with the most reactive element) is:</p>
  <ul>
    <li>
      Group 1
      <ul>
        <li>Potassium</li>
        <li>Sodium</li>
      </ul>
    </li>
    <li>
      Group 2
      <ul>
        <li>Calcium</li>
        <li>Magnesium</li>
      </ul>
    </li>
    <li>
      Group 3
      <ul>
        <li>Aluminium</li>
      </ul>
    </li>
    <li>Carbon</li>
    <li>
      Transition Metals
      <ul>
        <li>Zinc</li>
        <li>Iron</li>
      </ul>
    </li>
    <li>Hydrogen</li>
    <li>
      Precious Metals
      <ul>
        <li>Copper</li>
        <li>Silver</li>
        <li>Gold</li>
        <li>Platinum</li>
      </ul>
    </li>
  </ul>
  <p>Group 1 and 2 <strong>react violently</strong> with water to produce hydrogen gas. Transition metals will not react with water under normal conditions but <strong>will react with steam</strong>. Precious metals are <strong>less reactive than hydrogen</strong> and will not react with water or steam.</p>
  <p>Only elements less reactive than carbon can be extracted from their ores through reduction with carbon.</p>
